def area(a, h):
    """
    Возвращает площадь треугольника по заданному основанию и высоте.

    Параметры:
        a (float): основание треугольника.
        h (float): высота треугольника.

    Возвращает:
        float: площадь треугольника.

    Пример:
        >>> area(6, 4)
        12.0
    """
    return a * h / 2


def perimeter(a, b, c):
    """
    Возвращает периметр треугольника по заданным сторонам.

    Параметры:
        a (float): первая сторона треугольника.
        b (float): вторая сторона треугольника.
        c (float): третья сторона треугольника.

    Возвращает:
        float: периметр треугольника.

    Пример:
        >>> perimeter(3, 4, 5)
        12
    """
    return a + b + c
